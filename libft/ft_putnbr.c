#include "libft.h"

void	ft_putnbr(int a)
{
	if (a > 2147483647)
		return ;
	if (a == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (a < 0)
	{
		a *= -1;
		ft_putchar('-');
	}
	if (a < 10)
	{
		ft_putchar(a + '0');
		return ;
	}
	ft_putnbr(a / 10);
	ft_putchar((a % 10) + '0');
}
