#include "libft.h"

int			ft_decimal_length(int nbr)
{
	int		f;
	int		res;

	f = 10;
	res = 1;
	nbr *= (nbr < 0) ? -1 : 1;
	while (f <= nbr)
	{
		f *= 10;
		res++;
	}
	return (res);
}
