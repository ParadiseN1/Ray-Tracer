#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while (*(dest + i) != '\0')
		i++;
	while (*(src + k) != '\0')
	{
		*(dest + i + k) = *(src + k);
		k++;
	}
	*(dest + k + i) = '\0';
	return (dest);
}
