#include "libft.h"

int		ft_is_hex(const char *str)
{
	while (*str)
		if ((*str >= 'A' && *str <= 'F') ||
			(*str >= 'a' && *str <= 'f') ||
			(*str >= '0' && *str <= '9'))
			str++;
		else
			return (0);
	return (1);
}
